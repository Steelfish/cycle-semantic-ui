export namespace IconType {
  export function ToClassname(type: IconType | string) {
    if (typeof(type) === "string") {
      return " " + type;
    }
    if (type < 0)
    {
      return "";
    }
    let name = IconType[type];
    return " " + name.match(/[A-Z][a-z]*/g).join(" ").toLowerCase();
  }
}

